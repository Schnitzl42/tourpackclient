    private static final int PLACE_PICKER_REQUEST = 1;

    public void showPlacePicker(PlacePicker.IntentBuilder intentBuilder){
        Intent intent = null;
        try {
            intent = intentBuilder.build(this);
            startActivityForResult(intent, PLACE_PICKER_REQUEST);
        } catch (GooglePlayServicesRepairableException e) {
            e.printStackTrace();
        } catch (GooglePlayServicesNotAvailableException e) {
            e.printStackTrace();
        }
    }

    @Override
    protected void onActivityResult(int requestCode,
                                    int resultCode, Intent data) {

        if (requestCode == PLACE_PICKER_REQUEST
                && resultCode == Activity.RESULT_OK) {

            final Place place = PlacePicker.getPlace(this, data);
            final CharSequence name = place.getName();
            final CharSequence address = place.getAddress();
            String attributions = (String) place.getAttributions();
            if (attributions == null) {
                attributions = "";
            }
            LatLng coords = place.getLatLng();
            Log.d("place", "got name: "+name+" addr: "+address+" attr: "+attributions);
            //publish event
            EventBus.getDefault().post(new PlaceSelectedEvent(name.toString(),
                    address.toString(), Html.fromHtml(attributions).toString(), coords));

        } else {
            super.onActivityResult(requestCode, resultCode, data);
        }
    }
